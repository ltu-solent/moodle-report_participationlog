namespace report_participationlog\event;

class report_viewed extends \core\event\base {
    protected function init() {
        $this->data['crud'] = 'r';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Return localised event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('eventreportviewed', 'report_participationlog');
    }

    /**
     * Returns description of what happened.
     *
     * @return string
     */
    public function get_description() {
        return "The user with id '$this->userid' viewed the participation report for the user with id '$this->relateduserid'.";
    }

    /**
     * Returns relevant URL.
     *
     * @return \moodle_url
     */
    public function get_url() {
        return new \moodle_url('/report/participationlog/index.php', array(
            'userid' => $this->relateduserid,
            'action' => 'displaylogs',
            'startdate' => $this->other['startdate'] ?? null,
            'enddate' => $this->other['enddate'] ?? null
        ));
    }

    /**
     * Custom validation.
     *
     * @throws \coding_exception
     * @return void
     */
    protected function validate_data() {
        parent::validate_data();
        if (empty($this->relateduserid)) {
            throw new \coding_exception('The \'relateduserid\' must be set.');
        }
        if (empty($this->other['startdate'])) {
            throw new \coding_exception('The \'startdate\' must be set');
        }
        if (empty($this->other['enddate'])) {
            throw new \coding_exception('The \'enddate\' must be set');
        }
    }

}
